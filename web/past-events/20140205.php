        <h2>Responsive Workflow with Grunt & Collaging app in canvas</h2>
    </div>

    <p>We had a great meetup withg <a href="http://www.arrangeactassert.com/">Jag Rehal</a> talking about responsive workflow with Grunt and Jake Gordon
    talking about developing a collaging app with HTML5 for <a href="https://allyearbooks.co.uk/">AllYearbooks.co.uk</a></p>

    <p>Jag's talk on Grunt was fascinating and he covered lots of tips on how to use Grunt to help
with responsive deisgn. Matthew Pennell has an excellent <a href="http://www.thewatchmakerproject.com/post/jag-reehal-responsive-workflow-with-grunt-refresh-cambridge/">write-up on his blog</a>, and you can view <a href="http://operationmobile.github.io/rwd-using-grunt/#/">Jag's talk slides on GitHub</a>.</p>

    <p>Jake talked about how his team currently manually build photo collages for the yearbooks people order on <a href="https://allyearbooks.co.uk/">AllYearbooks.co.uk</a>. He's always wanted to use canvas in HTML5 to let people design their own collages but it's taken a few years for browsers to catch up. He took us through how he recently developed a canvas app to design collages, which looked really impressive. The canvas API makes a lot of this really easy, though Jake pointed out the mathmatics for some of the transformations he applies to the photos took some time to master!</p>

	<p>Our next meetup is planned to be Wed 2nd April, see you there!</p>
	
