        <h2>The Business of Web Design</h2>
    </div>

    <p>Joel Hughes came to talk to Refresh about his experiences freelancing and running a micro web agency. It was a fascinating talk
    covering lots of interesting topics such as how to get the right clients, how to grow a business and quite a few entertaining mistakes 
    to learn from! There was a lot of good advice and good discussion around business issues in the web design industry.</p>

    <p>The night was really well attended and lots of people hung around afterwards to chat. We even had the event covered by 
    Ed Goodman writing for <a href="http://www.cambridge-news.co.uk/Business/Business-News/A-developer-or-designer-Pick-Refresh-Cambridge-20140408102118.htm">Cambridge Evening News</a>
    I also wrote a <a href="http://www.studio24.net/blog/the-business-of-web-design/">blog post</a> on the night after Joel interviewed me for his upcoming podcast.</p>
    </p>

    <p>Joel is promoting his new conference <a href="http://thebusinessofwebdesign.co.uk/the-conference/">The Business of Web Design</a> 
    which is on in July in Cardiff. If you freelance, run a web agency, or are thinking about it then check it out!</p>

    <p>Drinks for the evening were kindly sponsored by <a href="https://workingatbooking.com/">Booking.com</a>.</p>

