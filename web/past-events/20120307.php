        <span>7<sup>th</sup> March 2012</span>
        <h2>CMSs for Designers</h2>
    </div>

    <p>We had around 20 people to listen to talks from Andy
        Field, Julian Price and Anthony Albertyn on ExpressionEngine,
        SilverStripe and Drupal. The discussions were lively after the talks, and it gave people new ideas on using CMS tools. We had a draw for a free "freelancer"
        license for ExpressionEngine, from the kind people at <a href="http://ellislab.com">EllisLabs</a> which Rachael Beale won.</p>

    <p>The event was kindly sponsored by <a href="http://www.creativefront.org">CreativeFront</a> who supplied free drinks and snacks, and was held at <a href="http://www.anglia.ac.uk">Anglia Ruskin University</a> who have hosted Refresh meetups for the past few years. </p>

