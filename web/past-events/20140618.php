        <h2>CreativeJS - Getting Artistic With Code</h2>
    </div>

    <p><a href="http://seb.ly/">Seb Lee-Delisle</a> is a digital artist and speaker who uses computers to engage with people and inspire them. He creates
    exciting digital art experiments such as <a href="http://pixelpyros.org/">PixelPyros</a> and <a href="http://lunartrails.com/">Lunar Trails</a>.</p>

    <p>Venturing into the unknown is scary, but fun things happen when you try new things. Seb discussed how it's
    rewarding to get out of your comfort zone and blur the boundaries between artist and coder. He talked about
    his recent experiments with lasers and of course, delivered some entertaining live demos.</p>

    <p>The evening, at the amazing <a href="http://www.computinghistory.org.uk/">Centre for Computing History</a>, was extremely well attended -
    you can watch Seb's talk on YouTube:</p>

