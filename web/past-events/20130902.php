        <h2>Running Large Websites &amp; Modern PHP</h2>
    </div>

    <p>We've recently kicked Refresh off again with a meetup in September at a new venue, the <a href="http://www.thecambridgebrewhouse.com">Cambridge Brew House</a></p>

    <p><a href="http://www.garethrushgrove.com/">Gareth Rushgrove</a> talked about the <a href="https://www.gov.uk/">Gov.uk project</a> and lessons he's learnt on running large websites. It was a fascinating talk that provoked lots of talk in the bar afterwards. Gareth's slides are available at <a href="https://speakerdeck.com/garethr/operating-gov-dot-uk">https://speakerdeck.com/garethr/operating-gov-dot-uk</a></p>

    <p><a href="http://www.simonrjones.net/">Simon Jones</a> talked about recent developments in PHP including regular release cycles, improvements to OO and the excellent new password_hash function in PHP 5.5. Simon's slides are available at <a href="http://www.slideshare.net/simonrjones/modern-php">http://www.slideshare.net/simonrjones/modern-php</a></p>

    <p>The event was kindly sponsored by <a href="http://www.studio24.net/">Studio 24</a> who provided a free bar!</p>
    
